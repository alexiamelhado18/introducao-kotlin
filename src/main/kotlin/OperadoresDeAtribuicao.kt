fun main(args: Array<String>) {
    /*
        =   atribui
        +=  soma
        -=  subtrai
        *=  multiplica
        /=  divide
        %=  porcetagem
    */

    var valor = 0;
    println("Operador =     $valor");

    valor += 5;
    println("Operador +=    $valor");

    valor -= 2;
    println("Operador -=    $valor");

    valor *= 2;
    println("Operador *=    $valor");

    valor /= 2;
    println("Operador /=    $valor");

    valor %= 100;
    println("Operador %=    $valor");
}