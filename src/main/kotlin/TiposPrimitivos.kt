fun main(args: Array<String>){
    val char: Char = 'M';
    val int: Int = 0;
    val float: Float = 0.0F;
    val string: String = "texto";
    val double: Double = 2000.00;
    val long: Long = 10_000_000_000_000L;

    println("Tipo char: $char");
    println("Tipo int: $int");
    println("Tipo float: $float");
    println("Tipo string: $string");
    println("Tipo double: $double");
    println("Tipo long: $long");
}