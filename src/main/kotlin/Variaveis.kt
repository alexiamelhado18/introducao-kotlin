fun main(args: Array<String>) {

    //val é uma variável imutável, ou seja, seu valor após ser definido na criação não pode ser modificado
    val pi = 3.14;

    //var é uma variável mutável, ou seja, seu valor pode ser modificado a qlqr momento
    var idade = 22;

    println("Valor de pi: " + pi);
    println("Minha idade em 2023 é:  " + idade);
}